export const DISPLAY_COPY = {
  'en-US': EN_US_COPY,
  'zh-CN': ZH_CN_COPY,
};

export function translate(language, key, replacements = {}) {
  const normalizedLanguage = DISPLAY_COPY[language] ? language : 'en-US';
  const template = DISPLAY_COPY[normalizedLanguage][key] ?? DISPLAY_COPY['en-US'][key] ?? key;

  return Object.entries(replacements).reduce(
    (message, [placeholder, value]) => message.replaceAll(`{${placeholder}}`, String(value)),
    template,
  );
}
